// 管理者区分から権限判定
// res=0:Deny all
// res=1:本人データのみAllow
// res=9:Allow all
// --------------------------------------------------
	public function authStaffSchedule($type=null){
		$type1 = substr($type,0,1);
		$type2 = substr($type,1,1);
		switch ($type1) {
			case 'S':
				return '9';
				break;
			case 'H':
				return '9';
				break;
			case 'B':
				if ($type2>'1') {
					return '1';
				}else{
					return '9';
				}
				break;
			default:
					return '0';
				break;
		}
	}


